package com.upgrad.movieapp.dto;

import java.time.LocalDateTime;

public class MovieDTO {

    private int movieId;
    private String movieName;
    private String movieDescription;
    private int duration;
    private String coverPhotoUrl;
    private String trailerPhotoUrl;
    private LocalDateTime releaseDate;

    public int getMovieId() {
        return movieId;
    }

    public void setMovieId(int movieId) {
        this.movieId = movieId;
    }

    public String getMovieName() {
        return movieName;
    }

    public void setMovieName(String movieName) {
        this.movieName = movieName;
    }

    public String getMovieDescription() {
        return movieDescription;
    }

    public void setMovieDescription(String movieDescription) {
        this.movieDescription = movieDescription;
    }

    public int getDuration() {
        return duration;
    }

    public void setDuration(int duration) {
        this.duration = duration;
    }

    public String getCoverPhotoUrl() {
        return coverPhotoUrl;
    }

    public void setCoverPhotoUrl(String coverPhotoUrl) {
        this.coverPhotoUrl = coverPhotoUrl;
    }

    public String getTrailerPhotoUrl() {
        return trailerPhotoUrl;
    }

    public void setTrailerPhotoUrl(String trailerPhotoUrl) {
        this.trailerPhotoUrl = trailerPhotoUrl;
    }

    public LocalDateTime getReleaseDate() {
        return releaseDate;
    }

    public void setReleaseDate(LocalDateTime releaseDate) {
        this.releaseDate = releaseDate;
    }
}
